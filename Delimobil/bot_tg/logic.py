import sqlite3

# Создание базы данных и подключения к ней
conn = sqlite3.connect('faq.db')
cursor = conn.cursor()

# Создание таблицы FAQ
cursor.execute('''
CREATE TABLE IF NOT EXISTS faq (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    question TEXT NOT NULL,
    answer TEXT NOT NULL
)
''')

# Вставка часто задаваемых вопросов
faqs = [
    ("Что такое Делимобиль?", "Делимобиль — это сервис каршеринга, который предоставляет возможность аренды автомобилей на короткий срок."),
    ("Автопарк", "Автопарк Делимобиля включает в себя разнообразные автомобили, которые можно арендовать на любые нужды."),
    ("Тарифы", "Тарифы Делимобиля зависят от времени аренды и выбранного автомобиля. Более подробную информацию можно найти на нашем сайте."),
    ("Акции и бонусы", "Мы регулярно проводим акции и предоставляем бонусы для новых и постоянных клиентов. Следите за новостями на нашем сайте.")
]

cursor.executemany('''
INSERT INTO faq (question, answer) VALUES (?, ?)
''', faqs)

# Сохранение изменений и закрытие подключения
conn.commit()
conn.close()

